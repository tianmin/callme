package com.diandian.mycall.message;

public class MessageDetail {

	private int smsDetailId;

	private int smsDetailThreadId;

	private String smsDetailAddress;

	private long smsDetailDate;

	private String smsDetailDateFormat;

	private int smsDetailType;

	private String smsDetailBody;

	public int getSmsDetailId() {
		return smsDetailId;
	}

	public void setSmsDetailId(int smsDetailId) {
		this.smsDetailId = smsDetailId;
	}

	public int getSmsDetailThreadId() {
		return smsDetailThreadId;
	}

	public void setSmsDetailThreadId(int smsDetailThreadId) {
		this.smsDetailThreadId = smsDetailThreadId;
	}

	public String getSmsDetailAddress() {
		return smsDetailAddress;
	}

	public void setSmsDetailAddress(String smsDetailAddress) {
		this.smsDetailAddress = smsDetailAddress;
	}

	public long getSmsDetailDate() {
		return smsDetailDate;
	}

	public void setSmsDetailDate(long smsDetailDate) {
		this.smsDetailDate = smsDetailDate;
	}

	public String getSmsDetailDateFormat() {
		return smsDetailDateFormat;
	}

	public void setSmsDetailDateFormat(String smsDetailDateFormat) {
		this.smsDetailDateFormat = smsDetailDateFormat;
	}

	public int getSmsDetailType() {
		return smsDetailType;
	}

	public void setSmsDetailType(int smsDetailType) {
		this.smsDetailType = smsDetailType;
	}

	public String getSmsDetailBody() {
		return smsDetailBody;
	}

	public void setSmsDetailBody(String smsDetailBody) {
		this.smsDetailBody = smsDetailBody;
	}

}
